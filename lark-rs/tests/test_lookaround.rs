//! Lookaround lowering oracles — the behavioral gate for the Lexer DFA / B1 plan
//! (`docs/LEXER_DFA_PLAN.md`).
//!
//! The B1 strategy retires `fancy-regex` by *lowering* every bounded lookaround
//! terminal onto the linear `regex` engine (a front-end that strips the assertion
//! plus boundary-assertion DFAs that validate it). These oracles pin the exact
//! lexing semantics that lowering must reproduce. Crucially they **pass against
//! today's `fancy-regex` lexer** — so committing them now (PR 1, the safety nets)
//! locks the behavior, and the B1 rewrite (PR 2/3) cannot silently diverge from it.
//!
//! Each of the four boundary-assertion forms (§4.2) has an isolated minimal grammar,
//! plus the two flag risks §6 flags:
//!
//!   * `trailing_neg_lookahead` — the **length-changing** case the bundled terminals
//!     never exercise: a greedy `a+` forced to a *shorter* match by a trailing
//!     `(?!b)` (`"aaab"` → `A="aa"`). This is the "longest end whose boundary
//!     assertion holds" rule (§4.2) the lowering must implement, not "match longest
//!     then peek".
//!   * `trailing_pos_lookahead` / `leading_pos_lookbehind` — the positive forms.
//!   * `leading_neg_lookbehind` — the even-backslash STRING-guard shape
//!     `(?<!\\)(?:\\\\)*"`, isolated so the reversed-body lookbehind DFA (§6
//!     reverse-DFA risk) has a dedicated adversarial oracle.
//!   * `inline_flag_in_assertion` / `global_flag_over_assertion` — a `(?i:…)` inline
//!     flag inside an assertion body, and `g_regex_flags=IGNORECASE` applied over an
//!     assertion; both must survive the front-end's strip/lower step (§6 flags risk).
//!
//! Oracle: `fixtures/oracles/lookaround/cases.json` (a list of `{name, grammar,
//! g_regex_flags, cases}` groups), generated by
//! `tools/generate_oracles.py::generate_lookaround`.

mod common;

use common::{load_oracle, tree_matches_oracle};
use lark_rs::grammar::terminal::flags;
use lark_rs::{Lark, LarkOptions, LexerType, ParserAlgorithm};

/// Map a serialized `g_regex_flags` name (the oracle's strings) to its flag bit —
/// the lockstep counterpart of `_RE_FLAGS` in `generate_oracles.py`.
fn flag_bit(name: &str) -> u32 {
    match name {
        "IGNORECASE" => flags::IGNORECASE,
        "MULTILINE" => flags::MULTILINE,
        "DOTALL" => flags::DOTALL,
        "VERBOSE" => flags::VERBOSE,
        other => panic!("unknown g_regex_flags name in oracle: {other:?}"),
    }
}

#[test]
fn test_lookaround_oracle() {
    let oracle = load_oracle("lookaround", "cases");
    let groups = oracle.as_array().expect("oracle must be a list of groups");

    let mut failures = Vec::new();

    for group in groups {
        let name = group["name"].as_str().expect("group needs a name");
        let grammar = group["grammar"].as_str().expect("group needs a grammar");

        let g_regex_flags = group["g_regex_flags"]
            .as_array()
            .map(|a| {
                a.iter()
                    .fold(0u32, |acc, v| acc | flag_bit(v.as_str().unwrap()))
            })
            .unwrap_or(0);

        // Build the parser exactly as Python Lark was built for the oracle: LALR +
        // contextual lexer + the group's global regex flags.
        let lark = Lark::new(
            grammar,
            LarkOptions {
                parser: ParserAlgorithm::Lalr,
                lexer: LexerType::Contextual,
                start: vec!["start".to_string()],
                g_regex_flags,
                ..Default::default()
            },
        )
        .unwrap_or_else(|e| panic!("{name}: grammar failed to build: {e}"));

        for case in group["cases"].as_array().expect("cases must be an array") {
            let input = case["input"].as_str().unwrap_or("");
            let should_pass = case["should_pass"].as_bool().unwrap_or(false);
            let oracle_ok = case["ok"].as_bool().unwrap_or(false);

            let result = lark.parse(input);

            match (should_pass, oracle_ok, &result) {
                (true, true, Ok(tree)) => {
                    if let Err(msg) = tree_matches_oracle(tree, &case["tree"]) {
                        failures.push(format!("{name} input={input:?}: tree mismatch: {msg}"));
                    }
                }
                (true, true, Err(e)) => {
                    failures.push(format!(
                        "{name} input={input:?}: expected parse success, got error: {e}"
                    ));
                }
                (false, false, Err(_)) => {}
                (false, false, Ok(_)) => {
                    failures.push(format!(
                        "{name} input={input:?}: expected parse failure, but parsing succeeded"
                    ));
                }
                _ => failures.push(format!(
                    "{name} input={input:?}: oracle/expectation disagree \
                     (should_pass={should_pass}, oracle_ok={oracle_ok})"
                )),
            }
        }
    }

    if !failures.is_empty() {
        panic!(
            "lookaround lowering oracle failures ({}):\n{}",
            failures.len(),
            failures.join("\n")
        );
    }
}
