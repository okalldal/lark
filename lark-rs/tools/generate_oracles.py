# ─── Oracle-contradiction registry (#253) ────────────────────────────────────
#
# A "contradiction" is a committed oracle case whose author-declared expectation
# (should_pass / should_parse / valid) disagrees with what Python Lark — the
# oracle (ADR-0001) — actually did. By default that is a bug in the case and must
# FAIL generation. Every contradiction is recorded here, then `finalize_contradictions()`
# (called at the end of __main__) cross-checks the detected set against the
# written allow-list `tools/oracle_contradictions.json` and exits non-zero on any
# un-allow-listed or stale entry.

_CONTRADICTIONS: list[str] = []


def note_contradiction(suite, kind, input_value, detail):
    """Record (and print) a case whose declared expectation disagrees with Python Lark.

    kind ∈ {"expected-parse","expected-reject","expected-valid"}. The KEY is
    language-neutral (json.dumps of the input) so the Rust meta-test can match it.
    `detail` is the volatile, human-facing message — printed, NOT part of the key.
    """
    key = f"{suite}: {kind} {json.dumps(input_value, ensure_ascii=False)}"
    _CONTRADICTIONS.append(key)
    print(f"  CONTRADICTION [{key}]: {detail}")
    return key


def finalize_contradictions():
    """Cross-check detected contradictions against the written allow-list.

    Writes the detected key set to tests/fixtures/oracles/_meta/contradictions.json
    (stable & idempotent, keys only) for the Rust meta-test, then exits non-zero if
    any contradiction is un-allow-listed or if any allow-list entry is stale (#253).
    """
    allow_path = TOOLS_DIR / "oracle_contradictions.json"
    allowed = {}
    if allow_path.exists():
        allowed = json.loads(allow_path.read_text()).get("entries", {})
    detected = sorted(set(_CONTRADICTIONS))
    # Always write the detected set (keys only — stable & idempotent) for the Rust meta-test.
    meta_dir = ORACLES_DIR / "_meta"
    meta_dir.mkdir(parents=True, exist_ok=True)
    (meta_dir / "contradictions.json").write_text(
        json.dumps({"_README": "Generated by tools/generate_oracles.py — do NOT hand-edit. "
                    "The set of oracle-case contradictions (author expectation vs Python Lark) "
                    "detected at generation time. Every key MUST appear in tools/oracle_contradictions.json "
                    "with a reason, or the generator exits non-zero (#253).",
                    "keys": detected}, indent=2, ensure_ascii=False) + "\n")
    un_allowed = [k for k in detected if k not in allowed]
    stale = [k for k in allowed if k not in set(detected)]
    if un_allowed or stale:
        if un_allowed:
            print("\nERROR: un-allow-listed oracle contradictions (Python Lark disagrees with the case's "
                  "declared expectation). Fix the case, or add to tools/oracle_contradictions.json with a reason:")
            for k in un_allowed:
                print(f"  + {k}")
        if stale:
            print("\nERROR: stale allow-list entries in tools/oracle_contradictions.json "
                  "(listed but no longer detected — remove them):")
            for k in stale:
                print(f"  - {k}")
        sys.exit(1)


def tree_to_dict(node):
    """Recursively convert a Lark parse tree to a serialisable dict."""
    if isinstance(node, Tree):
        return {
            "type": "tree",
            "data": node.data,
            "children": [tree_to_dict(c) for c in node.children],
        }
    elif isinstance(node, Token):
        return {
            "type": "token",
            "token_type": str(node.type),
            "value": str(node),
        }
    else:
        return {"type": "unknown", "repr": repr(node)}


def make_parser(grammar_text, parser="lalr", lexer="contextual", start="start"):
    return Lark(grammar_text, parser=parser, lexer=lexer, start=start)


